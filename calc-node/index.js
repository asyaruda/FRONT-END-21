function addCount(a, b) {
  return a + b
}

function subCount(a, b) {
  return a - b
}

function multCount(a, b) {
  return a * b
}

function divCount(a, b) {
  return a / b
}



module.exports.addCount = addCount
module.exports.subCount = subCount
module.exports.multCount = multCount
module.exports.divCount = divCount